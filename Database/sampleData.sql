--JOIN--
--LET'S GET USER NAME , TRANSACTION AMOUNT AND CATEGORY--
SELECT u.Name, t.AMOUNT, t.CATEGORY
FROM useer u
JOIN accounts a ON u.user_id=a.user_id
JOIN transactions t ON a.account_id=t.account_id;

--LET'S SEE USER NAME + BALANCE--
SELECT u.Name , a.BALANCE
FROM useer u
JOIN accounts a ON u.user_id=a.user_id;

--CATEGORY-WISE SPENDING--
SELECT CATEGORY , SUM(AMOUNT) AS TOTAL_SPENDING
FROM TRANSACTIONS
GROUP BY CATEGORY;

--TOTAL SPENDING PER ACCOUNT--
SELECT Account_ID, SUM(AMOUNT) AS TOTAL
FROM transactions
GROUP BY Account_ID;

--HIGHEST SPENDING FIRST--
SELECT Account_ID, SUM(AMOUNT) AS TOTAL
FROM transactions
GROUP BY Account_ID
ORDER BY TOTAL DESC;

--MONTHLY SPENDING--
SELECT TO_CHAR(TXN_DATE,'MM-YYYY')AS MONTH,
SUM(AMOUNT) FROM transactions
GROUP BY TO_CHAR(TXN_DATE,'MM-YYYY');

--category has highest spending--
SELECT CATEGORY , SUM(AMOUNT) AS TOTAL_SPENDING
FROM TRANSACTIONS
GROUP BY CATEGORY
ORDER BY SUM(AMOUNT) DESC;

--only spending > 5000 total--
SELECT CATEGORY , SUM(AMOUNT) AS TOTAL_SPENDING
FROM TRANSACTIONS
GROUP BY CATEGORY
HAVING SUM(AMOUNT)>5000;

